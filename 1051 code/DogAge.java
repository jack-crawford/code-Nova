//Calculates age in dog years

public class DogAge {
    //prints out human years and dog years
    
    public static void main (String[] args) {
      int humanYears = 8;
      int dogYears;
      dogYears = humanYears * 7;
      System.out.println("Age in human years: " + humanYears);
      System.out.println("Age in dog years: " + dogYears);
    }
}